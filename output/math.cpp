#include<iostream>
#include<math.h>
#include<stdlib.h>
using namespace std;
int main(){
    cout<<"ceil value is:"<<ceil(2.9)<<endl;
    cout<<"ceil value is:"<<ceil(-2.3)<<endl;
    cout<<endl;
    cout<<"floor value is:"<<floor(12.9)<<endl;
    cout<<"floor value is:"<<floor(-2.3)<<endl;
    cout<<endl;
    cout<<"round value is:"<<round(12.9)<<endl;
    cout<<"round value is:"<<round(-2.5)<<endl;
    cout<<endl;
    cout<<"sqrt value is:"<<sqrt(9)<<endl;
    cout<<"sqrt value is:"<<sqrt(25)<<endl;
    cout<<endl;
    cout<<"pow value is:"<<pow(9,2)<<endl;
    cout<<"pow value is:"<<pow(6,3)<<endl;
    cout<<endl;
    cout<<"abs value is:"<<abs(92)<<endl;
    cout<<"abs value is:"<<abs(-6)<<endl;
    cout<<endl;
    cout<<"rand value is:"<<rand()<<endl;
    cout<<"rand value is:"<<rand()%100<<endl;
    cout<<"rand value is:"<<rand()%50<<endl;
}